public class manejo_if_else {
    public static void main (String [] args){
        int x = 10;
        if(x  == 10){
            System.out.println("10 es igual a 10");
        }else if(x == 20){
           System.out.println("10 es menor a 20");
        }else if(x == 30){
            System.out.println("10 es menor a 30");
        }else {
            System.out.println("10 no es igual  a 10, ni a 20, ni a 30");
        }
    }
}
