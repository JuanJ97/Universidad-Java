public class Manejo_de_arreglos_persona {
    private String nombre;

    public Manejo_de_arreglos_persona(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return "Manejo_de_arreglos_persona{" + "nombre=" + getNombre() + '}';
    }
    
}
